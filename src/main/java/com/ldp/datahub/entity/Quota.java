package com.ldp.datahub.entity;

public class Quota {
	private int tagQuta;
	private int itemQuta;
	private int repoQuta;
	public int getTagQuta() {
		return tagQuta;
	}
	public void setTagQuta(int tagQuta) {
		this.tagQuta = tagQuta;
	}
	public int getItemQuta() {
		return itemQuta;
	}
	public void setItemQuta(int itemQuta) {
		this.itemQuta = itemQuta;
	}
	public int getRepoQuta() {
		return repoQuta;
	}
	public void setRepoQuta(int repoQuta) {
		this.repoQuta = repoQuta;
	}
}
